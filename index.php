      prioritaires de la police nationale, confirmées par la loi 
      d'orientation et de programmation pour la sécurité intérieure 
      d'août 2002  :
    </p>
    <ul class="dark_bullet">
        <li>
            <span class="bold"> La sécurité et la paix publiques </span>
            consistant à veiller à l'exécution des lois, à assurer 
            la protection des personnes et des biens, à prévenir 
            les troubles à l'ordre public et à la tranquillité publique 
            ainsi que la délinquance  ;
        </li>
        <li>
          <span class="bold"> La police judiciaire </span>
          ayant pour objet, sous la direction, le contrôle et la surveillance
            de l'autorité judiciaire, de rechercher et de constater les infractions 
            pénales, d'en rassembler les preuves, d'en rechercher les auteurs et 
            leurs complices, de les arrêter et de les déférer aux autorités 
            judiciaires compétentes  ;
        </li>
        <li>
            
          <span class="bold"> Le renseignement et l'information </span>
        , permettant d'assurer l'information des autorités gouvernementales, 
        de déceler et de prévenir toute menace susceptible de porter atteinte 
        à l'ordre public, aux institutions, aux intérêts fondamentaux de la 
        Nation ou à la souveraineté nationale.
        </li>
    </ul>
    <p class=" text-justify">
