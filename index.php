                    <div class="attribute-header">
                        <h1> Présentation générale </h1>
                    </div>
                    <div class="attribute-text">
                        <P class=" text-justify">
                        La déclaration des droits de l'homme et du citoyen de 1789, 
                        inscrite dans le préambule de constitution française, a défini 
                        la philosophie de l'action policière. Son premier article affirme 
                        que "la sûreté fait partie des droits inaliénables et 
                        imprescriptibles de l'homme" et le douzième que "la garantie des 
                        droits de l'homme et du citoyen nécessite une force publique 
                        instituée pour l'avantage de tous et non pour l'utilité particulière 
                        de ceux auxquels elle est confiée".
                        </p>
                    <p class=" text-justify">
                    Dans cet esprit républicain, la loi d'orientation et de programmation
                     relative à la sécurité de janvier 1995 a énoncé les missions 
                     prioritaires de la police nationale, confirmées par la loi 
                     d'orientation et de programmation pour la sécurité intérieure 
                     d'août 2002  :
                    </p>
                    <ul class="dark_bullet">
                        <li>
                            <b> La sécurité et la paix publiques </b>
                            consistant à veiller à l'exécution des lois, à assurer 
                            la protection des personnes et des biens, à prévenir 
                            les troubles à l'ordre public et à la tranquillité publique 
                            ainsi que la délinquance  ;
                        </li>
                        <li>
                          <b> La police judiciaire </b>
                          ayant pour objet, sous la direction, le contrôle et la surveillance
                           de l'autorité judiciaire, de rechercher et de constater les infractions 
                           pénales, d'en rassembler les preuves, d'en rechercher les auteurs et 
                           leurs complices, de les arrêter et de les déférer aux autorités 
                           judiciaires compétentes  ;
                        </li>
                        <li
                            <b>
                          <b> Le renseignement et l'information </b>
                        , permettant d'assurer l'information des autorités gouvernementales, 
                        de déceler et de prévenir toute menace susceptible de porter atteinte 
                        à l'ordre public, aux institutions, aux intérêts fondamentaux de la 
                        Nation ou à la souveraineté nationale.
                        </li>
                    </ul>
                    <p class=" text-justify">
                    Les missions assignées à la police nationale mettent en oeuvre l'ensemble 
                    de ses composantes. Aucune n'est l'apanage d'une direction et toutes les 
                    directions sont concernées, à titre principal ou accessoire, au premier 
                    chef ou en soutien, par les trois missions.
                    </p>
                    <p> Les synergies se concentrent selon cinq axes :</p>
                    <ul class="dark_bullet">
                        <li>
                          Assurer la sécurité des personnes, des biens et des institutions.
                        </li>
                        <li>
                         Maitriser les flux migratoires et lutter contre l'immigration illégale.
                        </li>
                        <li>
                          Lutter contre la criminalité organisée, la grande délinquance et la drogue.
                        </li>
                        <li>
                          Protéger le pays contre la menace extérieure et le terrorisme.
                        </li>
                        <li>
                          Maintenir l'ordre public.
                        </li>
                    </ul>
                    <p>
                        <b>
                            Assurer la sécurité des personnes, des biens et des institutions.
                        </b>
                    </p>
                    <p>
                        Peuvent être notamment cités à ce propos : </p>
                    <ul class="dark_bullet">
                        <li>
                        la direction centrale de la sécurité publique (DCSP), 
                        à titre principal, car, par son maillage territorial, 
                        elle  est le fer de lance de la police nationale dans 
                        la lutte contre la petite et moyenne délinquance. 
                        </li>
                        <li>
                        le service de la protection (SDLP), qui est chargé de 
                        la protection rapprochée des hautes personnalités françaises, 
                        ainsi que des hautes personnalités étrangères lorsqu'elles 
                        séjournent en France.
                        </li>
                    </ul>
                    <p>
                        <b>
                            Maitrisez les flux migratoires et lutter contre le travail 
                            clandestin.
                        </b>
                    </p>
                    <p class=" text-justify">
                    La direction centrale de la police aux frontières (DCPAF) veille 
                    au respect des règles relatives à la circulation transfrontière. 
                    Elle anime et coordonne au plan national, par l'intermédiaire de 
                    l'office central pour la répression de l'immigration irrégulière 
                    et de l'emploi d'étrangers sans titre, la lutte contre la délinquance 
                    en ces domaines.
                    </p>
                    <p>
                      <b>
                        Lutter contre la criminalité organisée, la grande délinquance 
                        et la drogue.
                      </b>
                    </p>
                    <p class=" text-justify">
                        Ce rôle est prioritairement dévolu à la direction centrale de 
                        la police judiciaire (DCPJ) qui dispose à cet effet d'offices 
                        centraux spécialisés.
                    </p>
                    <p>
                        <b>
                            Protéger le pays contre la menace extérieure et le terrorisme.
                        </b>
                    </p>
                    <p>
                        L'unité de coordination de la lutte antiterrorriste (UCLAT), rattaché
                        au directeur général de la police nationale, centralise les informations
                        fournies notamment par la DCRI et la DCPJ.
                    </p>
                    <p>
                        <b> Maintenir l'ordre public. </b>
                    </p>
                    <p class=" text-justify">
                    La direction centrale des compagnies républicaines de sécurité (DCCRS) 
                    et ses 61 unités mobiles constituent la réserve de la police nationale. 
                    Outre les opérations de maintien de l'ordre, auxquelles elle concourt, 
                    en particulier avec la D.C.S.P., elle participe à la sécurisation de la 
                    population dans les zones sensibles ainsi qu'à l'aide aux personnes 
                    (sécurité routière, en mer et en montagne).
                    </p>
                    <p>
                        L'unité d'assistance de recherche, d'intervention et de dissuasion (RAID)
                        apporte son soutien lors des opérations dans de nombreux domaines.
                    </p>
                    <p>
                        Ces missions et axes trouvent un echo hors des frontières par l'action de 
                        la direction de la coopération internationale (DCI) implantée dans 156 pays.
                    </p>
                    <p class=" text-justify">
                    Toutes les activités de la Police nationale sont mises en œuvre grâce aux 
                    fonctions d’administration, de gestion et de contrôle, assurées par la 
                    direction des ressources et des compétences de la Police nationale (DRCPN) 
                    et l’inspection générale de la Police nationale (IGPN), qui fournissent aux 
                    directions et services actifs des moyens suffisants et efficaces pour 
                    remplir leurs missions dans la meilleure éthique. La formation et le 
                    recrutement sont désormais assurés par la direction centrale du recrutement 
                    et de la formation de la Police nationale (DCRFPN).
                    </p>

